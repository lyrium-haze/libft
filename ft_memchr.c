#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	int				i;
	unsigned char	*str;

	str = (unsigned char *)s;
	i = 0;
	while (n > 0)
	{
		if (str[i] == (unsigned char)c)
		{
			return ((void *)str + i);
		}
		i++;
		n--;
	}
	return (NULL);
}
